alpha.intra.est <- function(path, keyword = c("Results", "Snapshot"), tburn) {
  
  # get the directory content
  # content <- list.files(paste("~/", path, sep = ""))
  content <- list.files(path)
  
  # order alphabetically
  content <- content[order(content)]
  
  # only the folders
  content <- grep(pattern = c("folder"), x = content, value = T)
  
  # loop over the local SA folders
  for (i in 1:length(content)) {
    
    # path to folder
    folder = paste(path, content[i], sep = "")
    print(paste("in localSA folder ", folder))
    
    # get content
    simFol = list.files(folder)
    
    # select only folders
    simFol <- grep(pattern = c(pattern), x = simFol, value = T)
    
    # loop over the sim folders
    for (j in 1:length(simFol)) {
      
      print(paste("in sim folder ", simFol[j]))
      
      # path to folder
      statsFol = paste(path, content[i], "/", simFol[j], "/stats-", simFol[j], sep = "")
      
      # get files
      results <- list.files(statsFol)
      
      # only csv files
      results <- grep(pattern = c("merged"), x = results, value = T)
      
      # only the results files
      results <- grep(pattern = c(keyword), x = results, value = T)
      
      # get name
      name <- paste(statsFol, "/", results, sep = "")
      
      # read table
      stats <- read.csv(name)
      
      # exclude burning ts
      subburn <- subset(stats, stats$timeStep >= tburn)
      
      # for each replicate, estimate alpha ii with the 2 methods
      repNb <- max(stats$replicate)
      
      # vector to store estimations
      alphaiiLM <- rep(0, repNb)
      riLM <- rep(0, repNb)
      alphaiiK <- rep(0, repNb)
      
      for (k in 1:repNb) {
        subrep <- subset(subburn, subburn$replicate == as.factor(k))
        
        # small transformation of the data to be able to fit Nt to deltaN
        Nt <- subrep$prey2PopulationSize[-length(subrep$prey2PopulationSize)]
        deltaNt <- subrep$prey2growthRate[-1] 
        
        # estimate with lm 
        linearMethod <- lm(formula = deltaNt/Nt ~ Nt)
        
        riLM[k] <- linearMethod$coefficients[1]
        alphaiiLM[k] <- -linearMethod$coefficients[2]/linearMethod$coefficients[1]
        
        # double check
        subrepNoburn <- subset(stats, stats$replicate == as.factor(k))
        NtNoburn <- subrepNoburn$prey2PopulationSize[-length(subrepNoburn$prey2PopulationSize)]
        deltaNtNoburn <- subrepNoburn$prey2growthRate[-1] 
        linearMethodNoburn <- lm(formula = deltaNtNoburn/NtNoburn ~ NtNoburn)
        
        par(mfrow = c(1,2))
        
        plot(y = deltaNt/Nt, x = Nt)
        abline(a = linearMethod$coefficients[1], b = linearMethod$coefficients[2], col = "red")
        
        plot(y = deltaNtNoburn/NtNoburn, x = NtNoburn)
        abline(a = linearMethodNoburn$coefficients[1], b = linearMethodNoburn$coefficients[2], col = "red")
        
      }
    }
  }  
}

